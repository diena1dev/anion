import org.bukkit.block.BlockType

object BlockLists {

	data class SimulatedData(
		val mass: Int,
		val hardness: Int,
	)

	private val simulatedBlockMap = mutableMapOf<BlockType, SimulatedData>()

	/** add [blockType] to sim map with given [mass] and [hardness] */
	fun registerSimulatedBlock(blockType: BlockType, mass: Int, hardness: Int) {
		simulatedBlockMap[blockType] = SimulatedData(mass, hardness)
	}

	/** if not present in list, assume it's unmovable and give a mass and hardness of MaxInt */
	fun getSimulatedBlock(blockType: BlockType): SimulatedData =
		simulatedBlockMap[blockType] ?: SimulatedData(Int.MAX_VALUE, Int.MAX_VALUE)

